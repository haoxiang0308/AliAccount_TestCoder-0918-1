% База фактов о семье на языке Prolog

% Факты о людях и их поле
мужчина(иван).
мужчина(андрей).
мужчина(сергей).
мужчина(павел).
женщина(мария).
женщина(ольга).
женщина(анна).
женщина(екатерина).

% Факты о родительских отношениях
родитель(иван, андрей).
родитель(иван, анна).
родитель(мария, андрей).
родитель(мария, анна).
родитель(андрей, павел).
родитель(ольга, павел).
родитель(сергей, екатерина).

% Правила
отец(X, Y) :- родитель(X, Y), мужчина(X).
мать(X, Y) :- родитель(X, Y), женщина(X).
сын(X, Y) :- родитель(Y, X), мужчина(X).
дочь(X, Y) :- родитель(Y, X), женщина(X).

% Брат и сестра
брат(X, Y) :- родитель(Z, X), родитель(Z, Y), мужчина(X), X \= Y.
сестра(X, Y) :- родитель(Z, X), родитель(Z, Y), женщина(X), X \= Y.

% Дедушка и бабушка
дедушка(X, Z) :- родитель(X, Y), родитель(Y, Z), мужчина(X).
бабушка(X, Z) :- родитель(X, Y), родитель(Y, Z), женщина(X).