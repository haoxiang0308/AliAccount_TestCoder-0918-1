<?php
// Проверяем, был ли запрос отправлен методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Пример обработки данных из формы
    // Предположим, в форме есть поля 'name' и 'email'
    $name = $_POST['name'] ?? '';
    $email = $_POST['email'] ?? '';

    // Простая валидация (в реальном приложении нужна более строгая валидация)
    if (empty($name) || empty($email)) {
        die("Имя и Email обязательны.");
    }

    // Пример сохранения в файл (в реальном приложении это может быть база данных)
    $data = "Имя: " . $name . ", Email: " . $email . " - " . date('Y-m-d H:i:s') . "\n";
    file_put_contents('form_data.txt', $data, FILE_APPEND | LOCK_EX);

    echo "Данные успешно сохранены!";
} else {
    // Если это не POST-запрос, можно вывести сообщение или перенаправить
    echo "Метод запроса не поддерживается.";
}
?>