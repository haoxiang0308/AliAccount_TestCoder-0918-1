# Создание скрипта R для построения диаграммы рассеяния

# Генерация случайных данных
set.seed(123)  # Для воспроизводимости результатов
x <- rnorm(100)  # 100 случайных значений из нормального распределения
y <- 2 * x + rnorm(100, sd = 0.5)  # y линейно связан с x с добавлением шума

# Создание диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X значения", 
     ylab = "Y значения",
     pch = 19,  # Тип точки (заполненные кружки)
     col = "blue"  # Цвет точек
)

# Добавление линии тренда
abline(lm(y ~ x), col = "red", lwd = 2)

# Генерация случайного имени файла
random_name <- paste0("scatter_plot_", sample(1000:9999, 1), ".png")

# Сохранение диаграммы в файл
png(random_name, width = 800, height = 600)
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X значения", 
     ylab = "Y значения",
     pch = 19, 
     col = "blue"
)
abline(lm(y ~ x), col = "red", lwd = 2)
dev.off()

cat("Диаграмма рассеяния сохранена в файл:", random_name, "\n")