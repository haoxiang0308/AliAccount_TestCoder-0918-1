<?php
// Проверяем, был ли запрос методом POST
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Получаем данные из формы
    $name = $_POST['name'] ?? '';
    $email = $_POST['email'] ?? '';

    // Простая валидация (можно расширить)
    if (!empty($name) && !empty($email)) {
        // Здесь можно добавить дополнительную логику, например, сохранение в базу данных
        // или отправку email. Для примера просто сохраним в файл.

        $data = "Имя: " . $name . "\nEmail: " . $email . "\n---\n";

        // Открываем файл для добавления данных
        $file = fopen('form_data.txt', 'a');
        if ($file) {
            fwrite($file, $data);
            fclose($file);
            echo "Данные успешно сохранены.";
        } else {
            echo "Ошибка при сохранении данных.";
        }
    } else {
        echo "Пожалуйста, заполните все поля формы.";
    }
} else {
    echo "Доступ запрещен.";
}
?>