# array_operations.jl
# This script demonstrates basic array operations in Julia.

# Define an example array
arr = [1, 2, 3, 4, 5]

# Calculate the sum
sum_result = sum(arr)

# Calculate the mean
mean_result = mean(arr)

# Print the results
println("Array: ", arr)
println("Sum: ", sum_result)
println("Mean: ", mean_result)
