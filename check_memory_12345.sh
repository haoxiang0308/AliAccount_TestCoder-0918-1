#!/bin/bash

# 脚本：check_memory.sh
# 功能：检查系统内存使用情况并显示

echo "检查系统内存使用情况..."
echo "-----------------------------"

# 使用 free 命令获取内存信息，并用 awk 格式化输出
free -h | awk 'NR==1{printf "%-10s %-10s %-10s %-10s %-10s %-10s\n", $1, $2, $3, $4, $5, $6} NR==2{printf "%-10s %-10s %-10s %-10s %-10s %-10s\n", $1, $2, $3, $4, $5, $6}'

echo "-----------------------------"
echo "内存检查完成。"