<?php
// Обработка формы и сохранение данных в файл

// Проверяем, был ли запрос методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Собираем данные из формы
    $name = $_POST['name'] ?? '';
    $email = $_POST['email'] ?? '';

    // Проверяем, что поля не пустые (простая валидация)
    if (!empty($name) && !empty($email)) {
        // Подготавливаем строку для записи
        $data = "Имя: " . $name . ", Email: " . $email . "\n";

        // Определяем имя файла для сохранения (например, data.log)
        $filename = "data.log";

        // Открываем файл для дозаписи (или создаем, если его нет)
        $file = fopen($filename, "a");

        if ($file) {
            // Записываем данные в файл
            fwrite($file, $data);
            fclose($file);

            // Возвращаем успешный ответ
            echo "Данные успешно сохранены в файл '$filename'.";
        } else {
            // Ошибка при открытии файла
            http_response_code(500);
            echo "Ошибка: не удалось открыть файл '$filename' для записи.";
        }
    } else {
        // Ошибка, если поля пустые
        http_response_code(400);
        echo "Ошибка: Пожалуйста, заполните все поля формы (имя и email).";
    }
} else {
    // Ошибка, если запрос не POST
    http_response_code(405);
    echo "Метод запроса не поддерживается. Используйте POST.";
}
?>