# Метод для вычисления факториала числа
def factorial(n)
  # Проверяем, является ли число неотрицательным
  if n < 0
    raise ArgumentError, "Факториал не определен для отрицательных чисел"
  end

  # Факториал 0 равен 1
  return 1 if n == 0

  # Вычисляем факториал
  (1..n).reduce(1, :*)
end

# Пример использования
puts factorial(5) # Выведет 120