# Скрипт R для построения диаграммы рассеяния
# Генерация случайных данных
set.seed(123)  # Для воспроизводимости результатов
x <- rnorm(100, mean = 50, sd = 10)
y <- 2 * x + rnorm(100, mean = 0, sd = 5)

# Создание диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния случайных данных",
     xlab = "Переменная X",
     ylab = "Переменная Y",
     pch = 19, 
     col = "blue")

# Добавление линии тренда
abline(lm(y ~ x), col = "red", lwd = 2)

# Сохранение графика в файл
png("scatter_plot.png")
plot(x, y, 
     main = "Диаграмма рассеяния случайных данных",
     xlab = "Переменная X",
     ylab = "Переменная Y",
     pch = 19, 
     col = "blue")
abline(lm(y ~ x), col = "red", lwd = 2)
dev.off()