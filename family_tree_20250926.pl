% База фактов о семье

% Предикаты: родитель(Родитель, Ребенок).
родитель(иван, мария).
родитель(иван, алексей).
родитель(ольга, мария).
родитель(ольга, алексей).
родитель(алексей, дмитрий).
родитель(алексей, анастасия).
родитель(екатерина, дмитрий).
родитель(екатерина, анастасия).

% Предикат: пол(Имя, Пол).
пол(иван, мужской).
пол(ольга, женский).
пол(алексей, мужской).
пол(мария, женский).
пол(екатерина, женский).
пол(дмитрий, мужской).
пол(анаstасия, женский).

% Правила:
% Является ли X отцом Y?
отец(X, Y) :- родитель(X, Y), пол(X, мужской).

% Является ли X матерью Y?
мать(X, Y) :- родитель(X, Y), пол(X, женский).

% Являются ли X и Y братьями или сестрами?
брат_сестра(X, Y) :- родитель(Z, X), родитель(Z, Y), X \= Y.

% Является ли X дедушкой Y?
дедушка(X, Y) :- отец(X, Z), родитель(Z, Y).

% Является ли X бабушкой Y?
бабушка(X, Y) :- мать(X, Z), родитель(Z, Y).