#!/bin/bash

# Скрипт для создания резервной копии файла
# Использование: ./backup_script.sh <имя_файла>

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: Не указан файл для резервного копирования."
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

# Получаем имя файла из первого аргумента
FILE_TO_BACKUP="$1"

# Проверяем, существует ли файл
if [ ! -f "$FILE_TO_BACKUP" ]; then
    echo "Ошибка: Файл '$FILE_TO_BACKUP' не найден."
    exit 1
fi

# Генерируем имя для резервной копии с временной меткой
BACKUP_NAME="${FILE_TO_BACKUP}_backup_$(date +%Y%m%d_%H%M%S)"

# Создаем резервную копию
cp "$FILE_TO_BACKUP" "$BACKUP_NAME"

# Проверяем, успешно ли выполнено копирование
if [ $? -eq 0 ]; then
    echo "Резервная копия создана: $BACKUP_NAME"
else
    echo "Ошибка при создании резервной копии."
    exit 1
fi