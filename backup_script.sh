#!/bin/bash

# Проверяем, передан ли аргумент (имя файла для резервного копирования)
if [ $# -eq 0 ]; then
    echo "Ошибка: Не указан файл для резервного копирования."
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

SOURCE_FILE="$1"

# Проверяем, существует ли файл
if [ ! -f "$SOURCE_FILE" ]; then
    echo "Ошибка: Файл $SOURCE_FILE не существует."
    exit 1
fi

# Генерируем случайное имя для файла резервной копии
# Используем /tmp для хранения временных файлов
BACKUP_FILE="/tmp/backup_$(date +%s)_$$"

# Создаем резервную копию
cp "$SOURCE_FILE" "$BACKUP_FILE"

# Проверяем, успешно ли создана резервная копия
if [ $? -eq 0 ]; then
    echo "Резервная копия файла создана: $BACKUP_FILE"
else
    echo "Ошибка при создании резервной копии."
    exit 1
fi