# Метод для вычисления факториала числа
def factorial(n)
  # Проверка на неотрицательность
  raise ArgumentError, "Факториал определен только для неотрицательных чисел" if n < 0

  # Базовые случаи
  return 1 if n == 0 || n == 1

  # Вычисление факториала с помощью reduce
  (1..n).reduce(:*)
end

# Пример использования:
# puts factorial(5) # => 120
# puts factorial(0) # => 1