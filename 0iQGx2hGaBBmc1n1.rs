// Структура пользователя с полями имя и возраст
#[derive(Debug, Clone)]
pub struct User {
    pub name: String,
    pub age: u32,
}

impl User {
    // Конструктор для создания нового пользователя
    pub fn new(name: String, age: u32) -> Self {
        User { name, age }
    }
}

fn main() {
    // Пример использования структуры
    let user = User::new("Иван".to_string(), 30);
    println!("Создан пользователь: {:?}", user);
    
    // Сохранение в файл (в бинарном формате с использованием serde, если бы была установлена зависимость)
    // Но так как мы не можем использовать внешние зависимости без установленного Cargo, 
    // просто создадим структуру в файле
}