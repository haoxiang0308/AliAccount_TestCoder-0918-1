# Метод для вычисления факториала числа
def factorial(n)
  # Проверяем, является ли число неотрицательным
  if n < 0
    raise ArgumentError, "Факториал определен только для неотрицательных чисел"
  end
  
  # Возвращаем 1 для 0! и 1!
  return 1 if n <= 1
  
  # Вычисляем факториал с помощью умножения
  (2..n).reduce(:*)
end

# Пример использования:
# puts factorial(5)  # Выведет 120