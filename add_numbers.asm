section .text
    global _start

; Функция сложения двух чисел
; Принимает два числа в регистрах eax и ebx, возвращает результат в eax
add_func:
    add eax, ebx
    ret

_start:
    ; Загружаем числа для сложения
    mov eax, 5      ; Первое число
    mov ebx, 3      ; Второе число

    ; Вызываем функцию сложения
    call add_func

    ; Результат теперь в eax (в данном случае, 8)

    ; Выход из программы
    ; Для систем Linux используем системный вызов exit
    mov ebx, 0      ; Код возврата
    mov eax, 1      ; Номер системного вызова (sys_exit)
    int 0x80        ; Вызов ядра