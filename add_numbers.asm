section .text
    global _start

; Функция для сложения двух чисел
; Принимает два аргумента в EBX и ECX, результат возвращается в EAX
add_numbers:
    mov eax, ebx    ; Копируем первое число в EAX
    add eax, ecx    ; Прибавляем второе число
    ret             ; Возвращаемся

_start:
    ; Загружаем числа для сложения в EBX и ECX
    mov ebx, 5      ; Первое число
    mov ecx, 10     ; Второе число

    ; Вызываем функцию сложения
    call add_numbers

    ; Результат теперь в EAX
    ; (В реальной программе здесь был бы код для вывода результата или его использования)

    ; Выход из программы
    mov eax, 1      ; Системный вызов sys_exit
    int 0x80        ; Вызов ядра