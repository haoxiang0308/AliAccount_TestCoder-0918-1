# Загрузка необходимых библиотек (не обязательно для базовой диаграммы рассеяния)
# library(ggplot2) 

# Создание случайных данных для примера
set.seed(42) # Для воспроизводимости
x <- rnorm(100)
y <- x + rnorm(100, sd = 0.5) # y зависит от x с небольшим шумом

# Построение диаграммы рассеяния
plot(x, y,
     main = "Диаграмма рассеяния",
     xlab = "X",
     ylab = "Y",
     pch = 19, # тип точки
     col = "blue" # цвет точки
)

# Сохранение графика в файл (например, PNG)
png("scatter_plot_output.png")
plot(x, y,
     main = "Диаграмма рассеяния",
     xlab = "X",
     ylab = "Y",
     pch = 19,
     col = "blue"
)
dev.off() # Закрытие устройства вывода
