-- Lua script that defines a function to add two numbers

-- Function to add two numbers
function add(a, b)
    return a + b
end

-- Example usage
print("Example usage of the add function:")
print("add(5, 3) =", add(5, 3))
print("add(-2, 7) =", add(-2, 7))
print("add(3.14, 2.86) =", add(3.14, 2.86))